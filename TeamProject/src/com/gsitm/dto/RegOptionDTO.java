public class RegOptionDTO {
	private int snackId;	// 간식 ID
	private int snackCount; // 간식 개수
	private int regInId;	// 예약 ID
	private int fixtureId;	// 기자재 ID

	public int getSnackId() {
		return snackId;
	}
	public void setSnackId(int snackId) {
		this.snackId = snackId;
	}
	public int getSnackCount() {
		return snackCount;
	}
	public void setSnackCount(int snackCount) {
		this.snackCount = snackCount;
	}
	public int getRegInId() {
		return regInId;
	}
	public void setRegInId(int regInId) {
		this.regInId = regInId;
	}
	public int getFixtureId() {
		return fixtureId;
	}
	public void setFixtureId(int fixtureId) {
		this.fixtureId = fixtureId;
	}


}
